void main(List<String> args) {
  // Operadores aritmeticos
  print(10 + 20);
  print(20 - 10);
  print(5 * 3);
  print(10 / 2);
  print(10 % 2);

  // Operadores de incremento e decremento
  int num1 = 1;
  num1++;

  print(num1);
  print(++num1);

  print(num1--);
  print(--num1);

  // Operadores relacionais
  int num2 = 5;
  int num3 = 10;

  print(num2 < num3);
  print(num2 > num3);
  print(num2 == num3);
  print(num2 != num3);
  print(num2 >= num3);
  print(num2 <= num3);

  // Operadores LÃ³gicos
  print(true && 10 == 20);
  print(false && 10 == 20);
  print(true && 10 == 10);
  print(true || false);
  print(true || true);
  print(false || false);

}
